#include<iostream>
using namespace std;
int main()
{     
    int val=3;
    switch(val){
    case 4:
    		cout<<"Value is four"<<endl;
    		break;
   	case 5:
    		cout<<"Value is five"<<endl;
    		break;
    case 3:
    		cout<<"Value is six"<<endl;
    		break;
    		default:
    			cout<<"Invalid"<<endl;
	}

	return 0;
}
